/*로그아웃 버튼 클릭 시 로그아웃 기능*/

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function logoutChk() {
	var logoutChk = confirm("로그아웃 하시겠습니까?");
	
	if(logoutChk == true) {
		alert("로그아웃 되었습니다.");
		window.location.href = '/login/logoutUser.do';
	} else {
		return false;
	}
}




